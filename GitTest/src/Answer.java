
public class Answer {
	private String kor,eng;

	public Answer(String kor, String eng) {
	this.eng= eng;
	this.kor= kor;
}

	public String getKor() {
		return kor;
	}

	public void setKor(String kor) {
		this.kor = kor;
	}

	public String getEng() {
		return eng;
	}

	public void setEng(String eng) {
		this.eng = eng;
	}
	
	
	
	

}
